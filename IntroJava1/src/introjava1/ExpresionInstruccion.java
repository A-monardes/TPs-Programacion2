public class ExpresionInstruccion {
    
    public static void main (String[] args){
        int x = 10; // Línea 1
        x = x + 5; // Línea 2
        System.out.println(x); // Línea 3
        
        /*La expresion en este codigo seria la operacion "x+5", siendo que una expresion es, justamente,
        una operación que el lenguaje de programación puede interpretar y manipular para poder llegar a un valor
        final. Las expresiones se encargan de los calculos del programa, mientras que las instrucciones se
        encargan de aplicar los cambios al programa. Una intruccion es cualquier comando que produzca un 
        cambio en el programa, como crear y asignar variables o llamar funciones.
        En este codigo podemos decir que todas las lineas del programa son una iintruccion: la linea 1 crea la
        variable "x", la segunda linea le asigna otro valor, y la tercer linea imprime en pantalla a esta variable.*/
    }
    
    
}
